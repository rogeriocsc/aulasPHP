<?php 
    $oper = 3;
    switch ($oper) {
        case 1:
            echo "Morango";
            break;
        case 2:
            echo "Banana";
            break;
        case 3:
            echo "Maça";
            break;
        case 4:
            echo "Pera";
            break;
        case 5:
            echo "Melancia";
            break;  
        
        default:
            echo "Nome Inválido!";
            break;
    }
    
?>

